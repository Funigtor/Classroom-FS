<h1>Aide pour l'utilisation de Classroom FS</h1>

<h2>Comment envoyer des fichiers?</h2>
<p>Pour envoyer des fichiers vers l'espace de travail, il suffit de faire un glisser-déposer dans le dossier de destination, à l'intérieur de la zone blanche présente en haut de chaque dossier.</p>

<h2>J'ai supprimé un fichier par erreur, comment le restaurer?</h2>
<p>Pour éviter la perte de données, les fichiers supprimés sont simplement masqués (ils sont renommés <a href="https://fr.wikipedia.org/wiki/Fichier_et_r%C3%A9pertoire_cach%C3%A9">de façon à commencer par un point</a>). Le seul moyen de restaurer un fichier supprimé par erreur est d'envoyer un e-mail à l'administrateur, en précisant bien le nom du fichier, ainsi que son emplacement.</p>

<h2>Comment sont triés les fichiers? Et les dossiers?</h2>
<p>Les fichiers sont triés par ordre d'envoi inversé (les plus récents en premier). Les dossiers, eux, sont triés par ordre alphabétique.</p>

<h2>Comment contacter l'administrateur?</h2>
<p>Pour contacter l'administrateur, vous pouvez envoyer un e-mail à <a href="mailto:<?php echo $config['admin_mail']; ?>"><?php echo $config['admin_mail']; ?></a>.</p>

